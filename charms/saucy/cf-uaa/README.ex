# Overview

This charm provides the Cloud Foundry [User Account and Authentication Service](https://github.com/cloudfoundry/uaa).

In Cloud Foundry, the User Account and Authentication Service (UAA) provides single sign-on for web applications and secures Cloud Foundry resources.
The UAA acts as an OAuth 2.0 Authorization Server. It grants access tokens to client applications for use in accessing Resource Servers in the platform, including the Cloud Controller.

# Usage

To deploy the Cloud Controller service:

    juju deploy cf-uaa

